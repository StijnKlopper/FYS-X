    void MeshFromPoints(List<Node> points)
    {
        if (points.Count < 1) return;
        AssignVertices(points);
        for (int i = 0; i < points.Count; i+= 3)
		{
            CreateTriangle(points[i], points[i + 1], points[i + 2]);
		}
    }

    void AssignVertices(List<Node> points)
    {
        for (int i = 0; i < points.Count; i++)
        {
            if (points[i].vertexIndex == -1)
            {
                points[i].vertexIndex = vertices.Count;
                vertices.Add(points[i].position);
            }
        }
    }

    void CreateTriangle(Node a, Node b, Node c)
    {
        triangles.Add(a.vertexIndex);
        triangles.Add(b.vertexIndex);
        triangles.Add(c.vertexIndex);

        Triangle triangle = new Triangle(a.vertexIndex, b.vertexIndex, c.vertexIndex);
        AddTriangleToDictionary(triangle.vertexIndexA, triangle);
        AddTriangleToDictionary(triangle.vertexIndexB, triangle);
        AddTriangleToDictionary(triangle.vertexIndexC, triangle);
    }

    void AddTriangleToDictionary(int vertexIndexKey, Triangle triangle)
    {
        if (triangleDictionary.ContainsKey(vertexIndexKey))
        {
            triangleDictionary[vertexIndexKey].Add(triangle);
        }
        else
        {
            List<Triangle> triangleList = new List<Triangle>();
            triangleList.Add(triangle);
            triangleDictionary.Add(vertexIndexKey, triangleList);
        }
    }

    struct Triangle
    {
        public int vertexIndexA;
        public int vertexIndexB;
        public int vertexIndexC;
        int[] vertices;

        public Triangle(int a, int b, int c)
        {
            vertexIndexA = a;
            vertexIndexB = b;
            vertexIndexC = c;

            vertices = new int[3];
            vertices[0] = a;
            vertices[1] = b;
            vertices[2] = c;
        }

        public int this[int i]
        {
            get
            {
                return vertices[i];
            }
        }


        public bool Contains(int vertexIndex)
        {
            return vertexIndex == vertexIndexA || vertexIndex == vertexIndexB || vertexIndex == vertexIndexC;
        }
    }


    public class CubeGrid
    {
        public Cube[, ,] cubes;

        public CubeGrid(int[, ,] map, float squareSize)
        {
            int nodeCountX = map.GetLength(0);
            int nodeCountY = map.GetLength(1);
            int nodeCountZ = map.GetLength(2);
            float mapWidth = nodeCountX * squareSize;
            float mapHeight = nodeCountZ * squareSize;

            ControlNode[, ,] controlNodes = new ControlNode[nodeCountX, nodeCountY, nodeCountZ];


            for (int x = 0; x < nodeCountX; x++)
            {
                for (int y = 0; y < nodeCountY; y++)
                {
                    for (int z = 0; z < nodeCountZ; z++) {
                        Vector3 pos = new Vector3(-mapWidth / 2 + x * squareSize + squareSize / 2, -mapWidth / 2 + y * squareSize + squareSize / 2, -mapHeight / 2 + z * squareSize + squareSize / 2);
                        controlNodes[x, y, z] = new ControlNode(pos, map[x, y, z] == 0, squareSize);
                    }
                }
            }

            cubes = new Cube[nodeCountX - 1, nodeCountY - 1, nodeCountZ - 1];
            for (int x = 0; x < nodeCountX - 1; x++)
            {
                for (int y = 0; y < nodeCountY - 1; y++)
                {
                    for (int z = 0; z < nodeCountZ - 1; z++) {
                        cubes[x, y, z] = new Cube(
                            controlNodes[x, y, z], //v0
                            controlNodes[x, y + 1, z], //v1
                            controlNodes[x + 1, y + 1, z], //v2
                            controlNodes[x + 1, y, z], //v3
                            controlNodes[x, y, z + 1], //v4
                            controlNodes[x, y + 1, z + 1], //v5
                            controlNodes[x + 1, y + 1, z + 1], //v6
                            controlNodes[x + 1, y, z + 1] //v7
                            );
                    }
                }
            }
        }
    }
